    @Override
    public void keyPressed(KeyEvent e) {
        if(!snake.hilo.isAlive()){
            snake.comenzar();
        }
        if(!snake.inGame){
            snake.continuar();
            return;
        }
        switch (e.getKeyCode()) {
            case 37: case 65://A, izquierda
                if(snake.dir!=Snake.Dir.R){
                    snake.nextDir = Snake.Dir.L;
                }
                break;
            case 38: case 87://W, arriba
                if(snake.dir!=Snake.Dir.D){
                    snake.nextDir = Snake.Dir.U; 
                }
                break;
            case 39: case 68://D, derecha
                if(snake.dir!=Snake.Dir.L){
                    snake.nextDir = Snake.Dir.R; 
                }
                break;
            case 40: case 83://S, abajo
                if(snake.dir!=Snake.Dir.U){
                    snake.nextDir = Snake.Dir.D;
                }
                break;
        }
    }

